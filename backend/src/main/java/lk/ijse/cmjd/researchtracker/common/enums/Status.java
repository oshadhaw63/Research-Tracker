package lk.ijse.cmjd.researchtracker.common.enums;

/**
 * Enum representing project status
 * PLANNING - Project is being planned
 * ACTIVE - Project is currently active
 * ON_HOLD - Project is temporarily on hold
 * COMPLETED - Project has been completed
 * ARCHIVED - Project is archived
 */
public enum Status {
    PLANNING,
    ACTIVE,
    ON_HOLD,
    COMPLETED,
    ARCHIVED
}
